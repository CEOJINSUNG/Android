package edu.skku.map.pa2;

import android.content.Context;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.BaseAdapter;
import android.widget.TextView;

import java.util.ArrayList;

public class LeftAdapter extends BaseAdapter {
    ArrayList<Integer> arrayList;
    Context context;

    public LeftAdapter (ArrayList <Integer> arrayList, Context context) {
        this.arrayList = arrayList;
        this.context = context;
    }

    @Override
    public int getCount() {
        return arrayList.size();
    }

    @Override
    public Object getItem(int i) {
        return arrayList.get(i);
    }

    @Override
    public long getItemId(int i) {
        return i;
    }

    @Override
    public View getView(int i, View view, ViewGroup viewGroup) {
        if (view == null) {
            LayoutInflater inflater = (LayoutInflater) context.getSystemService(Context.LAYOUT_INFLATER_SERVICE);
            view = inflater.inflate(R.layout.left_layout, viewGroup, false);
        }

        TextView topText = view.findViewById(R.id.left_text);
        topText.setText(arrayList.get(i).toString());
        return view;
    }
}
